package alfa0013;

public class Principale {
    public static void main(String[] args) {// stampare i numeri crescende e dcrescende con il for 
        for (int i=10;i>1;i-- ){
            System.out.println("the value of i is :"+i);
        }
        System.out.println("\n");
        for (int j=1;j<10;j++){
            System.out.println("the value of i is :"+j);
        }
    }
}